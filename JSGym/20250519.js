/*
========================================
問題: 配列内の数値をすべて偶数にする関数
出典: https://jsgym.shiftb.dev/q/54RsCnbh53
再提出: 不要
========================================
*/

// ✅ 自分の解答
// ① 引数となる配列を定義
const numbers = [1, 3, 4, 7, 8, 10];

// ② お題を満たす関数の定義
// ここに関数定義のコードを書いてください。
const createEvenList = (numbers) => {
  return numbers.map((item) => {
    return item % 2 !== 0 ? item + 1 : item;
  })
}

// ③ 関数の実行と結果表示
// ここで関数を実行して、結果をconsole.logで表示してください。
console.log(createEvenList(numbers));

