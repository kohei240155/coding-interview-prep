const prices = [100, 200, 300];

// ② 商品価格を税込みに変換する関数を作成
// ここに関数のコードを書くんだ〜💕
const addTax = (prices) => prices.map((item) => Math.round(item * 1.1));

// ③ 関数の実行と結果の表示
// 作成した関数をここで実行してconsole.logで確認するよ！✨
console.log(addTax(prices));

