// ② オブジェクトのキーを取得する関数を定義
// ここに関数を定義するコードを書いてください。
const getKeyList = (person) => Object.keys(person);

// ③ 作成した関数を実行し、結果をconsole.logで表示
// ここに関数を実行するコードを書いてください。
console.log(getKeyList(person));

