const hour = 13;

// メッセージを返す関数を作成してください。
const greeting = (hour) => {
  if (hour >= 6 && hour <= 11) {
    return 'おはようございます！';
  } else if (hour >= 12 && hour <= 17) {
    return 'こんにちは！';
  } else {
    return 'こんばんは！';
  }
}

// 関数を実行して結果を表示しましょう。
console.log(greeting(hour));

