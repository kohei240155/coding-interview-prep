// ✅ 自分の解答
// ① 指定回数の定数
const targetNumber = 5;

// ② ゼロから指定された回数までの配列を返す関数
// ここに関数定義を書いてください
const countUpList = (targetNumber) => {
  let list = [];
  for (let i = 0; i <= targetNumber; i++) {
    list.push(i);
  }
  return list;
}

// ③ 関数を実行して結果をconsole.logで表示
// ここに関数を実行するコードを書いてください
console.log(countUpList(targetNumber));

