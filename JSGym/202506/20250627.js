/*
================================================================================
問題: 入力された文字列を逆順にする関数を作ろう
出典: https://jsgym.shiftb.dev/q/7Pyk9n74es
================================================================================
*/

// ✅ 自分の解答
// ① 引数となる定数の定義
const text = "hello";

// ② お題を満たす関数の定義
// ここに関数定義のコードを書いてください。
const reverseString = (text) => text.split('').reverse().join('');

// ③ 関数の実行と結果の表示
// ここに関数を実行してconsole.logで結果を出力してください。
console.log(reverseString(text));

