/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number[]}
 */
var twoSum = function(nums, target) {
    let map = new Map();

    for (let [idx, num] of nums.entries()) {
        let diff = target - nums[idx];

        if (map.has(diff)) {
            return [map.get(diff), idx];
        }

        map.set(nums[idx], idx);
    }
};

